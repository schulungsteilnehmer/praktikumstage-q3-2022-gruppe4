class Fach {
    boolean lk;
    String name;
    int note;

    public Fach(String name, boolean lk, int note) {
        this.lk = lk;
        this.name = name;
        this.note = note;
    }

    public String getName() {
        return name;
    }

    public boolean getLk() {
        return lk;
    }

    public int getNote() {
        return note;
    }

}



